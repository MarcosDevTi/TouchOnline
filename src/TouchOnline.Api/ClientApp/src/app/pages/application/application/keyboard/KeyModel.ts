export class KeyModel {
    constructor(id, k1, k2, v1, k3, v2) {
        this.id = id;
        this.key1 = k1;
        this.key2 = k2;
        this.value = v1;
        this.key3 = k3;
        this.value2 = v2;
    }
    id: string;
    key1: string;
    key2: string;
    value: string;
    key3: string;
    value2: string;
}
